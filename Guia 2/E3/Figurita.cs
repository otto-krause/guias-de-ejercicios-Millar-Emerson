namespace E3
{
    public class Figurita
    {
        public string nombre;
        public string pais;
        public string posicion;
        public int numerofigurita;

        public Figurita(string nombre, string pais, string posicion, int numerofigurita)
        {
            this.nombre = nombre;
            this.pais = pais;
            this.posicion = posicion;
            this.numerofigurita = numerofigurita;
        }
    }
}